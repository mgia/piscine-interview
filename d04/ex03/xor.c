#include "header.h"

char *getXor(char *a, char *b)
{
	for (int i = 0; i < 6; i++)
		a[i] = (a[i] - '0') ^ (b[i] - '0') + '0';
	return (a);
}

int toInt(char *bits)
{
	int n = 0;

	for (int i = 0; i < 6; i++)
		n += (bits[i] - '0') * (1 << (5 - i));
	return (n);
}
